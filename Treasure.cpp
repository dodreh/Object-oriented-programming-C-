#include "Treasure.hpp"

#include <string>

//---------------------------------------------------------------------------------------------------------------------
Treasure::Treasure(std::string name, int treasure_id) : name_(name), treasure_id_(treasure_id) {}

//---------------------------------------------------------------------------------------------------------------------
std::string Treasure::getName()
{
  return name_;
}

//---------------------------------------------------------------------------------------------------------------------
Treasure::~Treasure() {}

//---------------------------------------------------------------------------------------------------------------------
Treasure::Treasure(const Treasure &copy) : name_(copy.name_), treasure_id_(copy.treasure_id_), found_(copy.found_)
{
}

//---------------------------------------------------------------------------------------------------------------------
Treasure& Treasure::operator= (const Treasure &treasure)
{
  found_ = treasure.found_;
  name_ = treasure.name_;
  treasure_id_ = treasure.treasure_id_;

  return *this;
}

//---------------------------------------------------------------------------------------------------------------------
int Treasure::getTreasureId()
{
  return treasure_id_;
}

//---------------------------------------------------------------------------------------------------------------------
bool Treasure::getFound()
{
  return found_;
}

//---------------------------------------------------------------------------------------------------------------------
void Treasure::setFound()
{
  found_ = true;
}